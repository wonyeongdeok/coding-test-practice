/*
비트 연산은 숫자를 이진수(0과 1로 이루어진 숫자)로 변환하여 각 비트(자리)를 비교하거나 계산하는 방법입니다. 이 연산은 SQL이나 프로그래밍에서 플래그, 권한, 속성 등이 여러 값을 동시에 가질 때 유용합니다.

기본 개념
1. 이진수:
숫자를 2진수로 나타내는 것.
10 (십진수) → 1010 (이진수)
8 (십진수) → 1000 (이진수)

2. 비트 연산자:
비트 단위로 연산을 수행합니다.

AND (&): 두 비트가 모두 1인 경우에만 1.
OR (|): 두 비트 중 하나라도 1인 경우 1.
XOR (^): 두 비트가 다를 때만 1.
NOT (~): 비트를 반전(1 ↔ 0).
*/

/*
대장균 개체 수 출력
where 2번 형질 보유 하지 않음 and (1번 보유 or 3번 보유)
*/

SELECT  COUNT(*) AS COUNT
  FROM  ECOLI_DATA
 WHERE  GENOTYPE & 2 = 0
   AND  (GENOTYPE & 1 > 0 OR GENOTYPE & 4 > 0);
